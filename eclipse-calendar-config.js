/**
 * Returns a localized explanatory note for the calendar card.
 * The wording stays intentionally compact and points to the NASA visibility region.
 * @param {object} event Shared eclipse dataset entry.
 * @param {string} lang UI language key.
 * @returns {string} Localized note text.
 */
function localizedEclipseCalendarNote(event, lang) {
  let note = "";

  if (lang === "de") {
    note = "NASA-Sichtbarkeitsregion: " + event.regions + ".";
    if (event.path) {
      note += " Zentralzone: " + event.path + ".";
    }
    return note;
  }

  if (lang === "fr") {
    note = "Région NASA de visibilité : " + event.regions + ".";
    if (event.path) {
      note += " Ligne centrale : " + event.path + ".";
    }
    return note;
  }

  note = "NASA visibility region: " + event.regions + ".";
  if (event.path) {
    note += " Central path: " + event.path + ".";
  }
  return note;
}

/**
 * Builds localized eclipse calendar entries from the shared NASA-based dataset.
 * @param {string} lang UI language key.
 * @returns {Array<object>} Localized eclipse events ready for the calendar config.
 */
function localizedEclipseCalendarEntries(lang) {
  return window.PLANETENUHR_ECLIPSE_CALENDAR_DATA.map(event => {
    return {
      date: event.date,
      category: "eclipse",
      title: localizedEclipseCalendarTitle(event.kind, event.type, lang),
      visibility: localizedEclipseCalendarVisibility(event.kind, event.type, lang),
      note: localizedEclipseCalendarNote(event, lang)
    };
  });
}

/**
 * Appends localized eclipse entries to one language array and removes duplicates.
 * Existing handcrafted entries win when the same date and title already exist.
 * @param {Array<object>} baseEvents Existing language-specific calendar array.
 * @param {Array<object>} extraEvents Generated eclipse entries.
 * @returns {Array<object>} Sorted merged array without duplicate date-title pairs.
 */
function mergeLocalizedEclipseCalendarEntries(baseEvents, extraEvents) {
  const merged = [];
  const seen = new Set();

  baseEvents.forEach(event => {
    const key = event.date + "|" + event.title;
    if (!seen.has(key)) {
      seen.add(key);
      merged.push(event);
    }
  });

  extraEvents.forEach(event => {
    const key = event.date + "|" + event.title;
    if (!seen.has(key)) {
      seen.add(key);
      merged.push(event);
    }
  });

  merged.sort((left, right) => {
    if (left.date < right.date) {
      return -1;
    }
    if (left.date > right.date) {
      return 1;
    }
    return left.title.localeCompare(right.title);
  });

  return merged;
}

if (window.PLANETENUHR_EVENTS) {
  ["en", "de", "fr"].forEach(lang => {
    if (!Array.isArray(window.PLANETENUHR_EVENTS[lang])) {
      window.PLANETENUHR_EVENTS[lang] = [];
    }
    const generatedEntries = localizedEclipseCalendarEntries(lang);
    window.PLANETENUHR_EVENTS[lang] = mergeLocalizedEclipseCalendarEntries(window.PLANETENUHR_EVENTS[lang], generatedEntries);
  });
}
